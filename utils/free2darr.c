#include <stdlib.h>

void	freestrarr(char ***arr)
{
	int	i;

	i = 0;
	if (!arr || !(*arr))
		return ;
	while ((*arr)[i])
	{
		free((*arr)[i]);
		i++;
	}
	free((*arr));
	(*arr) = NULL;
}
